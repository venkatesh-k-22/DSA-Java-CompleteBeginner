package com.venky;

public class Primitives {
    public static void main(String[] args) {
        //a is a Identifier 67 is a literal
        //In primitive data types we don't have inbuilt functions to perform some operations.
        int a = 67;
        //c is a literal
        char ch = 'c';
        //we need to add f at the end of floating values to represent, it is a floating point datatype
        float fl = 34567.78f;
        double db = 7897826732863296.89;
        //we need to add L at the end of long type variables to represent it is a long type variable
        long ln = 3334534678688897897L;
    }
}
